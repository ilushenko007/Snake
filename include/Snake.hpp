// =====================================================================
// Snake.hpp
// ---------------------------------------------------------------------
// Класс Snake инкапсулирует всю логику самой змейки:
//   - хранит её сегменты;
//   - умеет двигаться на один шаг в заданном направлении;
//   - умеет проверить, столкнулась ли голова с собственным телом;
//   - умеет проверить, наступила ли голова на конкретную клетку.
//
// Сегменты хранятся в std::deque<Cell> - это структура, в которой
// быстро добавлять элементы в начало и удалять с конца. Именно это и
// нужно змейке: на каждом шаге мы добавляем новую голову в начало и
// убираем хвост в конце. А если фрукт съеден - хвост не убираем,
// и змейка вырастает на один сегмент.
// =====================================================================
#pragma once

#include <SFML/Graphics/RenderTarget.hpp>
#include <deque>

// Направление движения. enum class - "сильный" enum, который не
// смешивается с числами по ошибке.
enum class Direction { Up, Down, Left, Right };

// Координаты клетки на поле (целые числа - индексы по сетке).
struct Cell {
    int x;
    int y;

    // Сравнение клеток: пригодится для проверки "наступила ли голова
    // на этот фрукт" и "врезалась ли голова в своё тело".
    bool operator==(const Cell& other) const {
        return x == other.x && y == other.y;
    }
};

class Snake {
public:
    // Конструктор создаёт змейку начальной длины в центре поля,
    // направление - "вправо".
    Snake();

    // Сбросить змейку в начальное состояние (используется при рестарте).
    void reset();

    // Запросить смену направления. Сразу не меняем, потому что иначе
    // игрок мог бы развернуться на 180° за один кадр и врезаться в
    // собственную шею. Реальная смена происходит на следующем шаге.
    void setDirection(Direction newDirection);

    // Сделать ОДИН шаг в текущем направлении.
    // Параметр grow=true означает "не убирать хвост" - змейка вырастет.
    void step(bool grow);

    // Заглянуть на одну клетку вперёд в текущем направлении движения,
    // не двигая саму змейку. Нужно, чтобы заранее понять, попадёт ли
    // голова на фрукт (и тогда сделать шаг с grow=true).
    Cell peekNextHead() const;

    // Получить голову (первый сегмент). Передаём по ссылке, чтобы не
    // копировать.
    const Cell& head() const { return m_body.front(); }

    // Получить всё тело (для отрисовки и проверки занятых клеток).
    const std::deque<Cell>& body() const { return m_body; }

    // Текущая длина змейки.
    std::size_t length() const { return m_body.size(); }

    // Текущее направление (для подсказок в UI, если понадобится).
    Direction direction() const { return m_direction; }

    // Проверка: пересекается ли голова с каким-нибудь сегментом тела.
    // Если да - игра проиграна.
    bool collidesWithSelf() const;

    // Проверка: занята ли указанная клетка телом змейки.
    // Нужно при генерации фрукта, чтобы он не появился внутри змейки.
    bool occupies(const Cell& cell) const;

    // Нарисовать змейку. target - окно или иной приёмник рендера.
    void draw(sf::RenderTarget& target) const;

private:
    std::deque<Cell> m_body;            // сегменты: front() - голова
    Direction        m_direction;       // текущее направление
    Direction        m_pendingDirection; // куда повернёмся на след. шаге

    // Маленькая утилита: проверяет, что два направления противоположны.
    static bool isOpposite(Direction a, Direction b);
};
