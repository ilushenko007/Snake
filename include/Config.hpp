#pragma once

#include <SFML/Graphics/Color.hpp>
#include <SFML/System/String.hpp>
#include <cstdint>
#include <string>

namespace cfg {

// ---------------------------------------------------------------------
// Размеры игрового поля.
// ---------------------------------------------------------------------
// Поле состоит из клеток (cells). Игровое поле имеет размер
// COLS x ROWS клеток, каждая клетка - CELL_SIZE x CELL_SIZE пикселей.
constexpr int CELL_SIZE = 24;   // размер одной клетки в пикселях
constexpr int COLS      = 25;   // количество клеток по горизонтали
constexpr int ROWS      = 20;   // количество клеток по вертикали

// Полоса сверху для надписи "Счёт: ..." и "Состояние: ...".
// (заголовок окна тоже обновляется, но иметь подпись внутри удобнее).
constexpr int HUD_HEIGHT = 40;   // высота верхней полосы в пикселях

// Итоговый размер окна в пикселях.
constexpr int WINDOW_WIDTH  = COLS * CELL_SIZE;
constexpr int WINDOW_HEIGHT = ROWS * CELL_SIZE + HUD_HEIGHT;

// ---------------------------------------------------------------------
// Параметры геймплея.
// ---------------------------------------------------------------------
// Начальная длина змейки (количество сегментов).
constexpr int SNAKE_START_LENGTH = 4;

// Сколько шагов в секунду делает змейка в самом начале.
// Чем больше число - тем быстрее игра.
constexpr float INITIAL_STEPS_PER_SECOND = 8.0f;

// Максимальная скорость, выше которой ускорение не идёт
// (иначе игра становится неиграбельной).
constexpr float MAX_STEPS_PER_SECOND = 22.0f;

// На сколько увеличивается скорость за каждый съеденный фрукт.
constexpr float SPEED_INCREMENT = 0.4f;

// Сколько очков даёт один фрукт.
constexpr int FRUIT_SCORE = 10;

// ---------------------------------------------------------------------
// Цвета (палитра).
// ---------------------------------------------------------------------
// SFML использует тип sf::Color (RGB или RGBA, каждое значение 0..255).
inline const sf::Color COLOR_BACKGROUND   {  20,  20,  30 }; // фон поля
inline const sf::Color COLOR_HUD          {  10,  10,  20 }; // фон полосы счёта
inline const sf::Color COLOR_GRID         {  35,  35,  50 }; // линии сетки
inline const sf::Color COLOR_BORDER       { 200,  60,  60 }; // граница поля
inline const sf::Color COLOR_SNAKE_HEAD   {  80, 200, 120 }; // голова змейки
inline const sf::Color COLOR_SNAKE_BODY   {  50, 160,  90 }; // тело змейки
inline const sf::Color COLOR_FRUIT        { 230,  90,  90 }; // фрукт
inline const sf::Color COLOR_TEXT         { 230, 230, 230 }; // обычный текст
inline const sf::Color COLOR_TEXT_DIM     { 150, 150, 160 }; // вторичный текст
inline const sf::Color COLOR_TEXT_ACCENT  { 255, 200,  90 }; // выделенный пункт меню

// ---------------------------------------------------------------------
// Имена файлов и пути.
// ---------------------------------------------------------------------
// Шрифт лежит в assets/ рядом с исполняемым файлом
// (CMake копирует папку automatically после сборки).
inline const std::string FONT_PATH   = "assets/DejaVuSans.ttf";
inline const std::string SCORES_FILE = "scores.txt";

// Сколько лучших результатов хранить в таблице рекордов.
constexpr int MAX_SCORES = 10;

// Заголовок окна (изначальный; в игре он будет дополняться счётом).
inline const std::string WINDOW_TITLE = "Snake - Змейка";

// ---------------------------------------------------------------------
// Вспомогательная функция для корректного отображения кириллицы.
// ---------------------------------------------------------------------
// Наши исходники хранятся в кодировке UTF-8, и std::string в
// командах вида "Счёт: ..." содержит байты UTF-8. Но
// конструктор sf::Text(font, std::string, size) в SFML интерпретирует
// эти байты как Latin-1 - кириллица превращается в квадратики.
// Чтобы этого избежать, переводим std::string в sf::String через
// специальный метод fromUtf8, который корректно декодирует UTF-8.
inline sf::String toSfString(const std::string& s) {
    return sf::String::fromUtf8(s.begin(), s.end());
}

inline sf::String toSfString(const char* s) {
    std::string tmp(s);
    return sf::String::fromUtf8(tmp.begin(), tmp.end());
}

} // namespace cfg
