// =====================================================================
// ScoreManager.hpp
// ---------------------------------------------------------------------
// Управление таблицей рекордов:
//   - грузит scores.txt при старте;
//   - умеет добавить новый результат и пересортировать;
//   - умеет сохранить таблицу обратно в файл;
//   - умеет отдать список лучших результатов для отрисовки.
//
// Формат файла scores.txt - простой текстовый:
//     <имя> <очки>
//     <имя> <очки>
//     ...
// По одной записи на строку. Имя без пробелов.
// =====================================================================
#pragma once

#include <string>
#include <vector>

// Одна запись в таблице рекордов.
struct ScoreEntry {
    std::string name;
    int         score = 0;
};

class ScoreManager {
public:
    ScoreManager() = default;

    // Загрузить таблицу из файла. Если файла нет - просто молча
    // продолжаем с пустой таблицей. Это нормальная ситуация при
    // первом запуске.
    void load();

    // Сохранить таблицу в файл.
    void save() const;

    // Добавить новый результат, отсортировать по убыванию очков и
    // оставить только лучшие MAX_SCORES записей. Возвращает true,
    // если результат попал в таблицу рекордов.
    bool tryAdd(const std::string& name, int score);

    // Получить таблицу для отрисовки.
    const std::vector<ScoreEntry>& entries() const { return m_entries; }

    // Текущий лучший результат (0, если таблица пуста).
    int bestScore() const;

private:
    std::vector<ScoreEntry> m_entries;
};
