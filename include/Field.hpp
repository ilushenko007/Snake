// =====================================================================
// Field.hpp
// ---------------------------------------------------------------------
// Класс Field отвечает за отрисовку статичной части игрового мира:
//   - фон поля;
//   - сетка (тонкие линии между клетками - помогает игроку видеть,
//     куда движется змейка);
//   - граница (красная рамка), при столкновении с которой - проигрыш.
//
// Никакой игровой логики тут нет, только отрисовка.
// =====================================================================
#pragma once

#include <SFML/Graphics/RenderTarget.hpp>

class Field {
public:
    Field() = default;

    // Нарисовать фон, сетку и границу. Вызывается каждый кадр первым.
    void draw(sf::RenderTarget& target) const;
};
