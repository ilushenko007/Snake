// =====================================================================
// Fruit.hpp
// ---------------------------------------------------------------------
// Фрукт - это просто клетка на поле, в которой нарисован красный
// кружок. Когда змейка съедает фрукт, мы вызываем respawn() и фрукт
// "переезжает" в новую случайную свободную клетку.
// =====================================================================
#pragma once

#include "Snake.hpp"   // нужно для Cell и для класса Snake (проверка занятости)

#include <SFML/Graphics/RenderTarget.hpp>
#include <random>

class Fruit {
public:
    // Конструктор сразу создаёт фрукт в случайной свободной клетке.
    explicit Fruit(const Snake& snake);

    // Переместить фрукт в новую случайную свободную клетку поля.
    // Передаём ссылку на змейку, чтобы не поставить фрукт в её клетку.
    void respawn(const Snake& snake);

    // Где сейчас фрукт.
    const Cell& position() const { return m_cell; }

    // Нарисовать фрукт.
    void draw(sf::RenderTarget& target) const;

private:
    Cell                              m_cell;     // текущая клетка
    std::mt19937                      m_rng;      // генератор случайных чисел
};
