// ---------------------------------------------------------------------
// Точка входа в программу. Всё, что мы делаем тут - создаём объект
// Game и зовём его метод run().
// Такая "тонкая" main() - хорошая практика: вся логика лежит в классах,
// а main() лишь запускает приложение.
// =====================================================================
#include "Game.hpp"

#include <exception>
#include <iostream>

int main() {
    try {
        Game game;
        return game.run();
    }
    catch (const std::exception& e) {
        // Если внутри игры что-то рухнуло (например, не найден шрифт),
        // покажем понятное сообщение, а не "молчаливое" падение.
        std::cerr << "[FATAL] " << e.what() << '\n';
        return 1;
    }
}
